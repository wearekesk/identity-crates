//! PKCS#3 Diffie-Hellman engine.
//!
//! Implements the key-exchange primitives used by PACE:
//! - [`DhParameterSpec`] — group parameters `(p, g, length)`.
//! - [`DhKeyPair`] — `(public_key, private_key)` pair.
//! - [`DHpkcs3Engine`] — engine for generating key pairs, computing shared
//!   secrets, and deriving the ephemeral generator for PACE-GM.
//!
//! Arithmetic uses [`num_bigint::BigUint`] since DH operates on non-negative
//! integers modulo a prime `p`. Random private keys are drawn from
//! `[2^(length-1), 2^length)`.

use num_bigint::BigUint;
use num_traits::{One, Zero};
use rand::rand_core::UnwrapErr;
use rand::{Rng, SeedableRng, rngs::StdRng, rngs::SysRng};
use thiserror::Error;

/// Default private-key length (bits) when not specified on the
/// [`DhParameterSpec`].
pub const DEFAULT_PRIVATE_KEY_LENGTH: u32 = 256;

// ---------------------------------------------------------------------------
// Errors
// ---------------------------------------------------------------------------

/// Error returned by [`DHpkcs3Engine`] operations.
#[derive(Debug, Error)]
#[error("DHpkcs3EngineError: {0}")]
pub struct DhPkcs3EngineError(pub String);

// ---------------------------------------------------------------------------
// DhParameterSpec
// ---------------------------------------------------------------------------

/// Diffie-Hellman group parameters.
#[derive(Debug, Clone)]
pub struct DhParameterSpec {
    p: BigUint,
    g: BigUint,
    length: u32,
    /// Order `q` of the prime-order subgroup generated by `g`, when known.
    /// When set, private keys are drawn uniformly from `[1, q-1]`, which avoids
    /// the modulo-bias that results from sampling a full `length`-bit value
    /// (since `q < 2^length`).
    q: Option<BigUint>,
}

impl DhParameterSpec {
    /// Creates a new [`DhParameterSpec`] with the given prime `p`, generator
    /// `g`, and private-key bit `length`.
    pub fn new(p: BigUint, g: BigUint, length: u32) -> Self {
        Self {
            p,
            g,
            length,
            q: None,
        }
    }

    /// Creates a new [`DhParameterSpec`] with [`DEFAULT_PRIVATE_KEY_LENGTH`].
    pub fn with_default_length(p: BigUint, g: BigUint) -> Self {
        Self::new(p, g, DEFAULT_PRIVATE_KEY_LENGTH)
    }

    /// Attaches the prime-order subgroup order `q`, enabling unbiased private
    /// key generation in `[1, q-1]`.
    pub fn with_subgroup_order(mut self, q: BigUint) -> Self {
        self.q = Some(q);
        self
    }

    /// Order of the prime-order subgroup, if known.
    pub fn q(&self) -> Option<&BigUint> {
        self.q.as_ref()
    }

    /// Private-key bit length.
    pub fn length(&self) -> u32 {
        self.length
    }

    /// Generator `g`.
    pub fn g(&self) -> &BigUint {
        &self.g
    }

    /// Prime modulus `p`.
    pub fn p(&self) -> &BigUint {
        &self.p
    }
}

impl std::fmt::Display for DhParameterSpec {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(
            f,
            "DhParameterSpec; p: {:?}, g: {:?}, length: {}",
            self.p.to_bytes_be(),
            self.g.to_bytes_be(),
            self.length,
        )
    }
}

// ---------------------------------------------------------------------------
// DhKeyPair
// ---------------------------------------------------------------------------

/// Diffie-Hellman key pair.
#[derive(Debug, Clone)]
pub struct DhKeyPair {
    public_key: BigUint,
    private_key: BigUint,
}

impl DhKeyPair {
    /// Creates a new [`DhKeyPair`].
    pub fn new(public_key: BigUint, private_key: BigUint) -> Self {
        Self {
            public_key,
            private_key,
        }
    }

    /// Public key.
    pub fn public_key(&self) -> &BigUint {
        &self.public_key
    }

    /// Private key.
    pub fn private_key(&self) -> &BigUint {
        &self.private_key
    }

    /// Renders the private key in addition to the public key — debugging only.
    pub fn to_string_also_private(&self) -> String {
        format!(
            "DhKeyPair; PublicKey: {:?}, PrivateKey: {:?}",
            self.public_key.to_bytes_be(),
            self.private_key.to_bytes_be(),
        )
    }
}

impl std::fmt::Display for DhKeyPair {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(
            f,
            "DhKeyPair; PublicKey: {:?} ",
            self.public_key.to_bytes_be()
        )
    }
}

// ---------------------------------------------------------------------------
// DHpkcs3Engine
// ---------------------------------------------------------------------------

/// PKCS#3 Diffie-Hellman engine. Holds a [`DhParameterSpec`] and a key pair;
/// derives shared secrets and the PACE-GM ephemeral generator.
#[derive(Debug, Clone)]
pub struct DHpkcs3Engine {
    parameter_spec: DhParameterSpec,
    public_key: BigUint,
    private_key: BigUint,
}

impl DHpkcs3Engine {
    /// Constructs an engine from a fixed `private_key`.
    pub fn from_private(
        private_key: BigUint,
        parameter_spec: DhParameterSpec,
    ) -> Result<Self, DhPkcs3EngineError> {
        Self::new(parameter_spec, Some(private_key), None)
    }

    /// Constructs an engine; if `private_key` is `None`, a new key pair is
    /// generated (optionally deterministic given `seed`).
    pub fn new(
        parameter_spec: DhParameterSpec,
        private_key: Option<BigUint>,
        seed: Option<u64>,
    ) -> Result<Self, DhPkcs3EngineError> {
        let priv_key = match private_key {
            Some(p) => p,
            None => Self::generate_private_key(&parameter_spec, seed)?,
        };
        // Reject degenerate / out-of-range private keys. A zero key yields a
        // trivial public key; a key >= the (sub)group order is outside the
        // valid private-key range.
        if priv_key.is_zero() {
            return Err(DhPkcs3EngineError(
                "Private key must not be zero".to_string(),
            ));
        }
        let upper = parameter_spec.q().unwrap_or_else(|| parameter_spec.p());
        if &priv_key >= upper {
            return Err(DhPkcs3EngineError(format!(
                "Private key must be less than the {} order",
                if parameter_spec.q().is_some() { "subgroup" } else { "group" }
            )));
        }
        let pub_key = Self::generate_public_key(&priv_key, &parameter_spec);
        Ok(Self {
            parameter_spec,
            public_key: pub_key,
            private_key: priv_key,
        })
    }

    /// Parameters used by this engine.
    pub fn parameter_spec(&self) -> &DhParameterSpec {
        &self.parameter_spec
    }

    /// Public key.
    pub fn public_key(&self) -> &BigUint {
        &self.public_key
    }

    /// Private key.
    pub fn private_key(&self) -> &BigUint {
        &self.private_key
    }

    /// Returns a [`DhKeyPair`] snapshot of the engine's keys.
    pub fn key_pair(&self) -> DhKeyPair {
        DhKeyPair::new(self.public_key.clone(), self.private_key.clone())
    }

    /// Computes the shared secret `otherPublicKey^privateKey mod p`.
    pub fn compute_secret_key(&self, other_public_key: &BigUint) -> BigUint {
        other_public_key.modpow(&self.private_key, self.parameter_spec.p())
    }

    /// Computes the PACE-GM ephemeral generator:
    /// `G_ephemeral = (g^nonce mod p * H) mod p` where `H` is the shared
    /// secret derived from `other_public_key`.
    pub fn compute_generator(&self, other_public_key: &BigUint, nonce: &BigUint) -> BigUint {
        let h = self.compute_secret_key(other_public_key);
        let g_exp = self
            .parameter_spec
            .g()
            .modpow(nonce, self.parameter_spec.p());
        (g_exp * h) % self.parameter_spec.p()
    }

    // -----------------------------------------------------------------------
    // Internal helpers
    // -----------------------------------------------------------------------

    fn generate_private_key(
        spec: &DhParameterSpec,
        seed: Option<u64>,
    ) -> Result<BigUint, DhPkcs3EngineError> {
        // When the subgroup order `q` is known, draw x uniformly from [1, q-1]
        // — the correct, unbiased range for a DH private key. Falling back to a
        // raw `length`-bit draw (when q is absent) is biased because q < 2^len.
        if let Some(q) = spec.q() {
            // Guard against a degenerate subgroup order: with q <= 1 the range
            // [1, q-1] is empty and the rejection-sampling loop would never
            // terminate.
            if q <= &BigUint::one() {
                return Err(DhPkcs3EngineError(format!(
                    "Invalid subgroup order q - must be greater than 1, got {q}"
                )));
            }
            let keep_bits = q.bits();
            let byte_len = (keep_bits as usize).div_ceil(8);
            let upper = q.clone();
            let lower = BigUint::one();
            let mut bytes = vec![0u8; byte_len.max(1)];
            return Ok(match seed {
                Some(s) => {
                    Self::draw_in_range(&mut StdRng::seed_from_u64(s), &mut bytes, &lower, &upper, keep_bits)
                }
                None => {
                    Self::draw_in_range(&mut UnwrapErr(SysRng), &mut bytes, &lower, &upper, keep_bits)
                }
            });
        }

        let length = spec.length;
        if length == 0 || length % 8 != 0 {
            return Err(DhPkcs3EngineError(format!(
                "Invalid bitLength value - {length}"
            )));
        }
        let byte_len = (length / 8) as usize;
        let lower = BigUint::one() << (length - 1);
        let upper = BigUint::one() << length;
        let mut bytes = vec![0u8; byte_len];
        // The buffer is exactly `length` bits wide, so no high bits to mask;
        // the [2^(length-1), 2^length) check selects the top-bit-set half.
        let keep_bits = length as u64;

        match seed {
            Some(s) => {
                let mut rng = StdRng::seed_from_u64(s);
                Ok(Self::draw_in_range(&mut rng, &mut bytes, &lower, &upper, keep_bits))
            }
            None => {
                let mut rng = UnwrapErr(SysRng);
                Ok(Self::draw_in_range(&mut rng, &mut bytes, &lower, &upper, keep_bits))
            }
        }
    }

    /// Rejection-samples a value in the half-open range `[lower, upper)`.
    ///
    /// `keep_bits` is the number of significant bits to retain from the drawn
    /// buffer: the high bits of the leading byte beyond `keep_bits` are masked
    /// off so each candidate already fits in `[0, 2^keep_bits)`. Choosing
    /// `keep_bits = upper.bits()` keeps the rejection probability below 50% per
    /// iteration regardless of where `upper` sits relative to a byte boundary.
    fn draw_in_range<R: Rng>(
        rng: &mut R,
        buf: &mut [u8],
        lower: &BigUint,
        upper: &BigUint,
        keep_bits: u64,
    ) -> BigUint {
        let excess = (buf.len() as u64 * 8).saturating_sub(keep_bits);
        loop {
            rng.fill_bytes(buf);
            // Drop the surplus high bits of the leading byte (excess is 0..=7
            // for a tightly-sized buffer) so candidates stay in [0, 2^keep_bits).
            if excess > 0 {
                if let Some(first) = buf.first_mut() {
                    *first &= 0xFFu8 >> excess;
                }
            }
            let n = BigUint::from_bytes_be(buf);
            if &n >= lower && &n < upper {
                return n;
            }
        }
    }

    fn generate_public_key(private_key: &BigUint, spec: &DhParameterSpec) -> BigUint {
        spec.g().modpow(private_key, spec.p())
    }
}

